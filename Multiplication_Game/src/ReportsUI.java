    public ReportsUI(User currentUser) {
        initComponents();
        
        this.currentUser = currentUser;
        
        mainReports = new ArrayList<String[]>();
        detailedReports = new ArrayList<ArrayList<String[]>>();
        
        ArrayList<String[]> fullReports = FileOperations.loadReportsFromFile(reportFileName);
        
        //Raporları iki parçaya ayırma
        for (String[] fullReport : fullReports) {
            
            //Raporların ana kısmını ayırma
            String[] tmpMainRepList = {fullReport[0], fullReport[1], fullReport[2], fullReport[3], fullReport[4], fullReport[5]};
            mainReports.add(tmpMainRepList);
            
            //Raporların sorularla ilgili kısmını ayaırma
            ArrayList<String[]> tmpDetailRepList = new ArrayList<String[]>();
            
            //Sorulan soruları ayırma
            String[] questionList = fullReport[6].split(":");
            for (int i = 0; i < questionList.length; i++) {
                
                //Sorulan soruların özelliklerini ayırma
                String[] questionPropertiesList = questionList[i].split("/");
                tmpDetailRepList.add(questionPropertiesList);
            }
            detailedReports.add(tmpDetailRepList);
        }
        
        mainReportTableModel = new CustomTableModel(reportsTable).loadMainReportTableModel(mainReports);
    }

