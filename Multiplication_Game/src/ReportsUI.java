    private void showDetailsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showDetailsButtonActionPerformed

        int selectedRow = reportsTable.getSelectedRow();
        
        if(selectedRow == -1){
            
            if(reportsTable.getRowCount() == 0){
                
                JOptionPane.showMessageDialog(this, "Rapor Yok", "Hata!", JOptionPane.ERROR_MESSAGE);
            }else{
                
                JOptionPane.showMessageDialog(this, "Lütfen Tablodan Rapor Seçiniz", "Hata!", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            
            dispose();
            new DetailedReportUI(currentUser, detailedReports.get(selectedRow)).setVisible(true);
        }
    }//GEN-LAST:event_showDetailsButtonActionPerformed

